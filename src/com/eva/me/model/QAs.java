package com.eva.me.model;

/**
 * @author violi
 *
 */
public class QAs {

	public String qus;
	public String ans;
	public float sco;
	/**
	 * @return the qus
	 */
	public String getQus() {
		return qus;
	}
	/**
	 * @param qus the qus to set
	 */
	public void setQus(String qus) {
		this.qus = qus;
	}
	/**
	 * @return the ans
	 */
	public String getAns() {
		return ans;
	}
	/**
	 * @param ans the ans to set
	 */
	public void setAns(String ans) {
		this.ans = ans;
	}
	/**
	 * @return the sco
	 */
	public float getSco() {
		return sco;
	}
	/**
	 * @param sco the sco to set
	 */
	public void setSco(float sco) {
		this.sco = sco;
	}
}
